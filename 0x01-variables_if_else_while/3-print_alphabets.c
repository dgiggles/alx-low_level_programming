#include <stdio.h>

/**
 * main - code that prints the alphs in lowercase, and then in uppercase,
 * only use putchar
 * Return: 0
 */

int main(void)
{
	int lw = 'a';
	int up = 'A';
	while (lw <= 'z')
	{
		putchar(1w);
		lw += 1;
	}
	while (up <= 'Z')
	{
		putchar(up);
		up += 1;
	}
		putchar('\n');
	return (0);
}
